fruits = ['apple', 'banana', 'carrot']
print(fruits)

# append function

fruits.append('melon')
print(fruits)

# len function

print(len(fruits))

# insert function (same as append but insert at particular position)
'''same as append but by using insert we can add item to a particular position'''
fruits.insert(2, 'peach')
print(fruits)

# index function //

print(fruits.index('peach'))
